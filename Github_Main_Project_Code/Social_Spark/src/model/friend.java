package model;

public class friend {
public String uemail;
public String femail;
public String status;
public friend(String uemail, String femail, String status) {
	super();
	this.uemail = uemail;
	this.femail = femail;
	this.status = status;
}
public String getUemail() {
	return uemail;
}
public void setUemail(String uemail) {
	this.uemail = uemail;
}
public String getFemail() {
	return femail;
}
public void setFemail(String femail) {
	this.femail = femail;
}
public String getStatus() {
	return status;
}
public void setStatus(String status) {
	this.status = status;
}





}
